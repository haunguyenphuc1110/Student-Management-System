        jComboBoxDay2.addItem(obj[0]);
        jComboBoxDay2.setSelectedIndex(0);
        jComboBoxMonth2.removeAllItems();
        jComboBoxMonth2.addItem(obj[1]);
        jComboBoxMonth2.setSelectedIndex(0);
        jComboBoxYear2.removeAllItems();
        jComboBoxYear2.addItem(obj[2]);
        jComboBoxYear2.setSelectedIndex(0);
        
        jTextFieldStatus.setText(dftable.getValueAt(selectedIndexRow, 10).toString());

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            SystemStudentManagementRemote session = (SystemStudentManagementRemote) init.getCtx().lookup(init.getJNDI());
            SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy");
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            Random rd = new Random();

            String name = jTextFieldName.getText();
                if ("".equals(name)) {
                    JOptionPane.showMessageDialog(null, "Please enter your name!!!");
                    jTextFieldName.requestFocus();
                    return;
                }
            
            String parentName = jTextFieldParentName.getText();
            
            String email = jTextFieldEmail.getText();
                if ("".equals(email)) {
                    JOptionPane.showMessageDialog(null, "Please enter your email!!!");
                    jTextFieldEmail.requestFocus();
                    return;
                }

            //Get birth from combobox
            Date birth;
            String day = jComboBoxDay.getSelectedItem().toString();
                if (day.equals("Day*")) {
                    JOptionPane.showMessageDialog(null, "Please choose day!!!");
                    jComboBoxDay.requestFocus();
                    return;
                }
                
            String month = jComboBoxMonth.getSelectedItem().toString();
                if (month.equals("Month*")) {
                    JOptionPane.showMessageDialog(null, "Please choose month!!!");
                    jComboBoxMonth.requestFocus();
                    return;
                }
            
            Date dateCurrent = new Date();
            String date = formatter.format(dateCurrent);
            String[] obj = date.split("-");
            
            String year = jComboBoxYear.getSelectedItem().toString();
                if (year.equals("Year*")) {
                    JOptionPane.showMessageDialog(null, "Please choose year!!!");
                    jComboBoxYear.requestFocus();
                    return;
                } else if((Integer.parseInt(obj[2]) -  Integer.parseInt(year))<5){
                    JOptionPane.showMessageDialog(null, "Please choose year because you are not old enough!!!");
                    jComboBoxYear.requestFocus();
                    return;
                }
            
            String stringBirth = day + "-" + month + "-" + year;
            Date datecreated = new Date();
            
            System.out.println(obj[2]);
            sdf.format(datecreated);
            
            //Check date is valid or not 
            if (checkValid(Integer.parseInt(day), Integer.parseInt(month), Integer.parseInt(year))) {
                birth = formatter.parse(stringBirth);
            } else {
                JOptionPane.showMessageDialog(null, "Birthday is not valid!!!");
                return;
            }
            

            String city = jComboBoxCity.getSelectedItem().toString();
                if (city.equals("Please choose city*")) {
                    JOptionPane.showMessageDialog(null, "Please choose city!!!");
                    jComboBoxCity.requestFocus();
                    return;
                }
            
            String phone = jTextFieldPhone.getText();
                if ("".equals(phone)) {
                    JOptionPane.showMessageDialog(null, "Please enter your phone!!!");
                    jTextFieldPhone.requestFocus();
                    return;
                }
                
            String course = jComboBoxCourse.getSelectedItem().toString();
                if (course.equals("Choose course*")) {
                    JOptionPane.showMessageDialog(null, "Please choose course!!!");
                    jComboBoxCourse.requestFocus();
                    return;
                }
            
            String info = jTextArea.getText();
            
           

            Request request = new Request();
            request.setId("TB" + rd.nextInt(101));
            request.setGuestname(name);
            request.setParentname(parentName);
            request.setEmail(email);
            request.setBirthday(birth);
            request.setAddress(city);
            request.setPhone(phone);
            request.setCourse(course);
            request.setInformation(info);
            request.setDatecreated(datecreated);
            request.setStatus("CXL");

            session.receiveRequest(request, "AD1");
            JOptionPane.showMessageDialog(null, "Send successfully!!!");
            
            //Write event
            date_event = new Date();
            sdf.format(date_event);
            logging.writeFileLog(filename,date_event +  " Guest"  + " send request\n");
            
            reset();
        } catch (NamingException | ParseException e) {
            Logger.getLogger(Registration.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnLoginActionPerformed


    private void loadDataOnJTable() {
        try {

            SystemStudentManagementRemote session = (SystemStudentManagementRemote) init.getCtx().lookup(init.getJNDI());
            List<Object[]> courseLists = session.getCourses();
            listCourse = new ArrayList<>();
            for (int i = 0; i < courseLists.size(); i++) {
                String id = (String) courseLists.get(i)[0];
                String name = (String) courseLists.get(i)[1];
                String duration = (String) courseLists.get(i)[2];
                int fee = (int) courseLists.get(i)[3];
                String type = (String) courseLists.get(i)[4];
                switch (type) {
                    case "KG":
                        type = "Kindergarten";
                        break;
                    case "KD":
                        type = "Kid";
                        break;
                    case "YT":
                        type = "Youth";
                        break;
                    case "ADU":
                        type = "Adult";
                        break;
                }

                listCourse.add(new Course(id,name, duration, fee, type));
                dftable = new DefaultTableModel();
                dftable = (DefaultTableModel) jNotificationTable.getModel();
                Object[] ojb = {name, duration, fee, type};
                dftable.addRow(ojb);
            }

        } catch (NamingException ex) {
            Logger.getLogger(CourseView.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }


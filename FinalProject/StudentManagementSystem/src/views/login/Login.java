    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            SystemLoginRemote session1 = (SystemLoginRemote) init1.getCtx().lookup(init1.getJNDI());
            SystemStudentManagementRemote session2 = (SystemStudentManagementRemote) init2.getCtx().lookup(init2.getJNDI());

            String filename = "log.txt";
            SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy HH:mm:ss");
            Date date_event = new Date();
            sdf.format(date_event);

            if (jCheckBoxGuest.isSelected() == true) {
                logging.writeFileLog(filename, date_event + " Guest " + " try to login the system\n");
                new GuestView().setVisible(true);
                this.dispose();
            } else {
                String _username = jTextFieldName.getText().toUpperCase();
                user_ID = _username;
                String _password = String.valueOf(jTextFiledPassword.getPassword()).toUpperCase();
                
                if (session1.findState(_username)) {
                    JOptionPane.showMessageDialog(null, "User has been logged in already");
                    System.exit(0);
                }

                if (_password.hashCode() != Integer.parseInt(session1.getUserPassword(_username))) {
                    JOptionPane.showMessageDialog(null, "Wrong username/password!");
                    jTextFieldName.setText("");
                    jTextFiledPassword.setText("");
                    jTextFieldName.requestFocus();
                    logging.writeFileLog(filename, date_event + " Someone " + " try to login the system with wrong username and password\n");
                } else {
                    if (_username.contains("AD")) {
                        session1.update_State(_username, true);
                        Object[] admin_name = session2.findAdminName(_username);
                        user_name = admin_name[1] + " " + admin_name[0];
                        logging.writeFileLog(filename, date_event + " " + user_ID + " try to login the system\n");
                        new AdminView().setVisible(true);
                        this.dispose();
                    } else {
                        session1.update_State(_username, true);
                        Object[] student_name = session2.findStudentName(_username);
                        user_name = student_name[1] + " " + student_name[0];
                        logging.writeFileLog(filename, date_event + " " + user_ID + " try to login the system\n");
                        new StudentView().setVisible(true);
                        this.dispose();
                    }
                }
            }
        } catch (NamingException ex) {
            Logger.getLogger(Login.class
                    .getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnLoginActionPerformed


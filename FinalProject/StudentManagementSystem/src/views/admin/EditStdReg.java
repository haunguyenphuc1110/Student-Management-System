    private void loadDataOnJTable() {
        try {            
            dftable.setRowCount(0);
            SimpleDateFormat formatter1 = new SimpleDateFormat("dd/MM/yyyy");
            SimpleDateFormat formatter2 = new SimpleDateFormat("yyyy-MM-dd");

            SystemStudentManagementRemote session = (SystemStudentManagementRemote) init.getCtx().lookup(init.getJNDI());
            List<Object[]> students = session.getAllStudents();
            countStudent = students.size();
            for(int i = 0 ; i < students.size() ; i++){
                String id_student = students.get(i)[0].toString();
                String first_name = students.get(i)[1].toString();
                String last_name = students.get(i)[2].toString();
                String student_birthday = formatter1.format(formatter2.parse(students.get(i)[3].toString()));
                String gender = ("true".equals(students.get(i)[4].toString()))?"Male":"Female";
                String email = students.get(i)[5].toString();
                String phone = students.get(i)[6].toString();
                String address = students.get(i)[7].toString();
                String admission = formatter1.format(formatter2.parse(students.get(i)[8].toString()));
                String username = students.get(i)[9].toString();
                String status = students.get(i)[10].toString();
                

                Object[] ojb = {id_student, first_name, last_name, student_birthday, gender, email, phone, address, admission, username, status};
                dftable.addRow(ojb);
            }
            jTextField1.setText(String.valueOf(countStudent));
        } catch (NamingException | ParseException ex) {
            Logger.getLogger(EditStdReg.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }


    private void jButtonFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFilterActionPerformed
        dftable.setRowCount(0);
        String key = jComboBoxType.getSelectedItem().toString();
        for (int i = 0; i < listCourse.size(); i++) {
            if (key.equals("All")){
                String name = listCourse.get(i).getName();
                String duration = listCourse.get(i).getDuration();
                int fee = listCourse.get(i).getFee();
                String type = listCourse.get(i).getType();
                Object[] ojb = {name, duration, fee, type};
                dftable.addRow(ojb);
            }
                
            if (listCourse.get(i).getType().equals(key)) {
                String name = listCourse.get(i).getName();
                String duration = listCourse.get(i).getDuration();
                int fee = listCourse.get(i).getFee();
                String type = listCourse.get(i).getType();
                Object[] ojb = {name, duration, fee, type};
                dftable.addRow(ojb);
            }

        }
    }//GEN-LAST:event_jButtonFilterActionPerformed

